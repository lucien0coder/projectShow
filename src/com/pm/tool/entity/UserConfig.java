package com.pm.tool.entity;

public class UserConfig {
	//屏幕数
	private Integer moniter;
	//展示类型
	private String type;
	//展示类型的扩展内容
	private String typeExtend;
	
	public Integer getMoniter() {
		return moniter;
	}
	public void setMoniter(Integer moniter) {
		this.moniter = moniter;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTypeExtend() {
		return typeExtend;
	}
	public void setTypeExtend(String typeExtend) {
		this.typeExtend = typeExtend;
	}
	
}
